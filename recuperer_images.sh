UPLOADED=0

echo "Téléchargement de $TOTAL images depuis l'ancien Supabase..."

for FILENAME in "${MISSING_URLS[@]}"; do
  OLD_URL="$OLD_BASE/$FILENAME"
  LOCAL="./images_recuperees/$FILENAME"

  # Télécharger depuis l'ancien Supabase
  HTTP=$(curl -s -o "$LOCAL" -w "%{http_code}" "$OLD_URL")
  
  if [ "$HTTP" = "200" ]; then
    # Uploader vers le nouveau Supabase
    MIME="image/jpeg"
    case "${FILENAME##*.}" in
      png|PNG) MIME="image/png" ;;
      webp) MIME="image/webp" ;;
    esac
    
    UP=$(curl -s -o /dev/null -w "%{http_code}" -X POST "$NEW_UPLOAD/$FILENAME" \
      -H "apikey: $NEW_KEY" \
      -H "Authorization: Bearer $NEW_KEY" \
      -H "Content-Type: $MIME" \
      -H "x-upsert: true" \
      --data-binary "@$LOCAL")
    
    if [ "$UP" -lt 300 ]; then
      UPLOADED=$((UPLOADED+1))
    fi
    DONE=$((DONE+1))
  fi

  echo -ne "\r$DONE/$TOTAL téléchargées, $UPLOADED uploadées"
done

